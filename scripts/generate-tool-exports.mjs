import { readdir, stat, writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const rootDir = dirname(fileURLToPath(import.meta.url));
const packageDir = join(rootDir, '..');
const toolsDir = join(packageDir, 'src', 'tools');
const outputFile = join(toolsDir, 'index.ts');

const entries = await readdir(toolsDir, { withFileTypes: true });
const toolNames = [];

for (const entry of entries) {
  if (!entry.isDirectory()) continue;

  const indexPath = join(toolsDir, entry.name, 'index.ts');

  try {
    const indexStat = await stat(indexPath);
    if (indexStat.isFile()) {
      toolNames.push(entry.name);
    }
  } catch {
    // Tool folders without index.ts are intentionally skipped.
  }
}

toolNames.sort((left, right) => left.localeCompare(right));

const lines = [
  '// This file is generated by scripts/generate-tool-exports.mjs.',
  '// Do not edit it manually.',
  '',
  ...toolNames.map((toolName) => `export * from './${toolName}/index.js';`),
  '',
];

await writeFile(outputFile, lines.join('\n'));
